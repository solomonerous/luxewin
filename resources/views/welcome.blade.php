         showPopup($(this).attr('data-popup'));
         return false;
     });

     function showPopup(el) {
         if ($('.popup').is('.active')) {
             $('.popup').removeClass('active');
         }
         $('.overlayed, body, .popup.' + el).addClass('active');
         $('.overlayed').removeClass('animation-closed');
     }
 </script>